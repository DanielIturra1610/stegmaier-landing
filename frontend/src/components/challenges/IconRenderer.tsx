/**
 * Hook para renderizar los iconos de categoría
 * @param category Categoría del desafío
 * @param className Clases CSS opcionales
 * @returns Componente SVG del icono correspondiente
 */
export const useCategoryIcon = (category: ChallengeCategory, className?: string): JSX.Element => {
  const iconName = React.useMemo(() => {
    // Usamos la constante importada en la parte superior
    return CATEGORY_ICONS[category];
  }, [category]);

  return <IconRenderer iconName={iconName} className={className} />;
};

